                <p>
                En los últimos años, hemos visto a más y más emisoras mexicanas anunciar compromisos ambientales, sociales y de gobierno corporativo (ASG). Es un hecho que las empresas se identifican, crecientemente, 
				como una pieza clave para contribuir con soluciones para mitigar el cambio climático o generar mejores oportunidades de desarrollo económico para las comunidades a las que sirven.
                </p>

                <p>
				Sin duda alguna, tales compromisos son bien recibidos por los diferentes grupos de interés de cada empresa. Ya sean sus empleados, consumidores, reguladores, inversionistas, o la sociedad en general, 
				los grupos de interés (conocidos en inglés como “stakeholders”) estamos no solo a la expectativa de más y mejores compromisos ASG, sino a la espera de planes detallados para transformar tales
				compromisos en realidades. La clave para lograrlo reside en contar con gobiernos corporativos más sólidos y transparentes.
				</p>

                <blockquote>
                  <p>
                    El gobierno corporativo
                  </p>
                </blockquote>

                <p>
                Se refiere a la estructura y el conjunto de procesos y políticas que rigen a una empresa. De acuerdo con Business Roundtable – una asociación conformada por los directores ejecutivos de las principales
				empresas públicas en Estados Unidos – la creación de valor a largo plazo es la métrica para evaluar si un gobierno corporativo es exitoso o no.[1] En la más reciente edición de su carta anual a CEOs,
				Larry Fink – Presidente y CEO de BlackRock – comparte su visión de que “en el mundo globalmente interconectado de hoy, una compañía debe crear valor y ser valorada por todos sus stakeholders con el fin
				de entregar valor a largo plazo a sus accionistas”.
				Dos piezas clave de un buen gobierno corporativo son, por una parte, su Consejo de Administración, conformado por individuos altamente capacitados para supervisar la ejecución de la estrategia de una 
				empresa; por el otro, un equipo ejecutivo que, valga la redundancia, ejecute tal estrategia.
				</p>

                <h3>¿Cómo construir gobiernos corporativos de alta calidad?</h3>
                <p>
                  El desempeño del consejo es un factor crítico para el éxito económico de una empresa, así como para la protección de los intereses de los accionistas. La pregunta entonces es, ¿por dónde empezar a armar un consejo de alto calibre?

				En los últimos años, las empresas mexicanas han progresado estableciendo consejos de mejor calidad. Tomando la diversidad de género como una entre varias métricas para medir el avance alcanzado, y de acuerdo con un estudio reciente 
				publicado por Deloitte y el Club del 30%, en 2021 las mujeres ocuparon el 9.7% de los asientos en los Consejos de Administración entre las emisoras mexicanas analizadas por el estudio. [2] Este número representa una mejora contra el
				6.5% reportado en 2018. Las industrias de manufactura, servicios financieros, y de bienes de consumo fueron las que reportaron una mayor presencia de mujeres consejeras.

				Sin embargo, este 9.7% aún está por debajo del promedio mundial. El mismo estudio reporta a nivel global que las mujeres representan el 19.7% en consejos entre las más de 10 mil empresas analizadas en 51 países; Francia, Noruega e 
				Italia son los países con un mayor porcentaje de mujeres consejeras. México ocupa el lugar número 41.  
                </p>
