                <blockquote>
                  <p>
                    Principios Fundamentales de la Auditoría Integral
                  </p>
                </blockquote>

                <p>
                  La auditoría integral es un concepto basado en dos principios fundamentales de la
                  Administración Pública:
                </p>
                <p>
                  El primer principio es que las Entidades Públicas deber ser conducidas de tal manera
                  que se haga el mejor uso posible de los fondos públicos. Los funcionarios responsables
                  de disponer de dichos fondos deben asegurarse de que sus decisiones son legales y
                  éticas, así como que dichas decisiones resulten en servicios públicos Eficientes,
                  Económicos y Eficaces.
                </p>
                <p>
                  El segundo principio es que las personas que tienen a su cargo las Entidades Públicas
                  deben rendir cuentas sobre la manera en que manejaron los recursos confiados a ellos.
                  Este principio de Rendición de Cuentas se aplica a la totalidad del sector público, desde
                  representantes electos quienes rinden a la sociedad hasta los funcionarios públicos,
                  quienes rinden a los representantes electos. En todos los niveles existe la obligación de
                  demostrar que se están siguiendo las prácticas administrativas adecuadas
                </p>
                <p>
                  Una Auditoría Integral implica el examen de controles, procesos y sistemas
                  utilizados para manejar los recursos de la Entidad: Dinero, Personas, Activo Fijo e
                  Información, esto nos da una idea de que esta auditoría puede abarcar una variedad de
                  áreas incluyendo:
                </p>
                <p>
                  -Planeación Financiera, Presupuestos, Contabilidad y Estados Financieros.
                </p>
                <p>
                  -Planeación, Desarrollo, Capacitación y Utilización de los Recursos Humanos.
                </p>
                <p>
                  -Planeación, Adquisición y Utilización de Propiedades, Planta, Equipo, Inventario
                   y Otros Activos.
                </p>
                <p>
                  -Desarrollo y Producción de la Información necesaria para planear, operar y
                  controlar a la Entidad, así como para cumplir con la obligación de rendir cuentas.
                </p>

                <blockquote>
                  <p>
                    Alcance del Trabajo de Auditoría
                  </p>
                </blockquote>
                <p>
                  El alcance ampliado de la auditoría de una Entidad del Gobierno, de un programa,
                  actividad o fiinción debe incluir;
                </p>
                <p>
                  Auditoría Financiera y de cumplimiento- Determina:
                  -Si los Estados Financieros de la Entidad auditada presentan razonablemente la
                  situación financiera y los resultados de las operaciones financieras están de acuerdo con
                  principios de contabilidad generalmente aceptados.
                  -Si la Entidad ha cumplido las leyes y reglamentos que tengan un efecto importante
                  sobre los Estados Financieros.
                </p>
                <p>
                  Auditoría de Eficiencia y Economía -Determina:
                  -Si la Entidad esta administrando y utilizando sus recursos (como pueden ser:
                  personal, propiedades, dinero y materiales) Eficientemente y Económicamente.
                  -Las causas de la falta de eficiencia o las practicas antieconómicas.
                  -Si la Entidad ha cumplido las leyes y reglamentos relacionados con los asuntos de
                  Eficiencia y Economía.
                </p>
                <p>
                  Auditoría de Resultados de programas -Determina:
                  Si se están alcanzando los resultados deseados o los beneficios establecidos por la
                  legislatura u otras organismos autorizados.
                  Si la Entidad ha considerado alternativas que proporcionen al menor costo los
                  resultados deseados.
                  Al determinar el alcance de una auditoría en particular, el funcionario responsable
                  de ella y la Entidad deben tomar en cuenta las necesidades de los usuarios potenciales de
                  los hallazgos de la auditoría.
                </p>

                <blockquote>
                  <p>
                    Términos de la auditoría integral
                  </p>
                </blockquote>
                <p>
                  La Auditoría Integral se identifica con los parámetros o términos que hacen o se
                  pretenden al desarrollarla, y que en este capítulo se describen los que se consideran
                  deben de indicarse en este tipo de trabajo.
                </p>
                <p>
                  Cada uno de los términos se pueden considerar en cada etapa de aplicación de la
                  Auditoría Integral, y estos son: Eficiencia, Economía y Eficacia, y al verificarlos estamos
                  determinando el grado con que son manejados los recursos de la Entidad para el logro
                  efectivo de las metas y objetivos que se persiguen o se quieren lograr en está
                </p>
                <p>-EFICIENCIA.</p>
                <p>-ECONOMIA.</p>
                <p>-EFICACIA.</p>
                <p>-RESPONSABILIDAD.</p>
                <p>-RENDIR CUENTAS.(Sus Resultados)</p>

