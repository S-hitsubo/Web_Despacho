              <h2 class="entry-title">
                <a href="blog-single.php">La auditoría integral como herramienta administrativa</a>
              </h2>
              <div class="entry-content" align="justify">
                <p>
                  La auditoría integral como evaluación preventiva, permite validar los sistemas administrativos, financiero, de cumplimiento y de gestión que gobiernan una entidad, con el objeto de proponer alternativas para el logro adecuado de sus fines y/o el mejor aprovechamiento de sus recursos.
                </p>
                <p>
                  La auditoría es tan antigua como el hombre mismo, ya que surgió la necesidad de registrar transacciones efectuadas  de tal manera que la contabilidad se complemente con la auditoria y que esta verifique el cumplimiento de las actividades y que los valores demuestren seguridad, confiabilidad y razonabilidad de sus transacciones comerciales, los métodos de evaluación para las empresas han venido cambiando significativamente debido a fraudes, errores, fracasos  y pérdidas financieras no detectadas ni controladas a tiempo.
                </p>
                <p>
                  La auditoría en las organizaciones se volvió tendencia mundial debido al alto porcentaje de fraudes y errores cometidos por el personal de las empresas debido a la escasa aplicación de auditorías, luego de tantos escándalos financieros se crearon normas que la regulen, así también se crearon lineamientos para que el auditor realice su trabajo de manera eficiente,  cumpliendo con los estándares de calidad y satisfaciendo de manera eficaz al control interno en una organización. La falta de control en los negocios han ocasionado el quiebre de empresas por la mala administración, y principalmente por falta de políticas y normas de control en las propias empresas.
                </p>
                <p>
                  En este enfoque de auditoria aportamos  un concepto diferente que ayude a los auditores evaluar holísticamente, con ayuda de varios especialistas en materia de contabilidad y auditoría, de tal modo que identifiquen los riesgos y controles claves para evitar posibles fallos en la empresa, y que se puedan cumplir los objetivos que  persiguen las organizaciones. Según Chiesa, nos indica que el proceso de auditoria tiene que ver con nuestra capacidad de innovación, ya que su metodología de auditoria va más allá de los problemas y necesidades, este modelo de innovación se refiere a la gestión de procesos implementados con ayuda de la tecnología.
                </p>
                <p>
                  permitie evaluar con un enfoque de sistemas, el grado y forma de cumplimiento de los objetivos de la institución, de la relación con su entorno, así como de sus operaciones, y para poder verificar los puntos débiles de la organización con el fin de tomar medidas y precauciones a tiempo.
				        </p>

                  <h3>
                    AUDITORIA INTEGRAL UNA REVISIÓN DE SUS COMPETENCIAS 
                  </h3>
                <p>
                  Este enfoque de auditoria es más amplio, evalúa de manera global los sistemas de gestión de los negocios, para un mejor cumplimiento de sus objetivos en las organizaciones. Según Viloria, nos específica que una visión más amplia para el auditor, tomando conceptos del holismo en base a este enfoque, para que se realice eficientemente esta auditoría integral, su objetivo principal es evaluar los sistemas de gestión que posee la empresa y que son establecidos por la administración con el objetivo de mejorar el rendimiento de todos sus recursos, cuando nos referimos a la auditoría integral esta comprende todas las partes indispensables de un todo en la organización, su estructura debe ser analizada en su conjunto y no por unidad, es importante mencionar que ayuda a la detección de fraudes y errores ocasionados por el personal administrativo de la entidad, el análisis global de la auditoria comprende la situación financiera en su totalidad.
                  Las auditorias integradas se han manifestado con rapidez, brindan grandes beneficios a las organizaciones, ya que se comenzó a recomendar que no solo se realizara una auditoria de estados financieros, sino también una auditoría integral, es decir que se evalúen holísticamente los riesgo que pudieran existir en la gestión administrativa en la empresa, con el objetivo de detectar a simple evaluación la situación administrativa de las normativas y las políticas, que se estén implementando en la empresa. Algunos autores, tales como, Porras, proponen un mejor concepto de auditoría integral, de tal manera que sirva en los procesos de evaluación, y para que este nuevo enfoque sea realizado con éxito.  Este nuevo enfoque está basado en los requerimientos de las Normas ISO 9001(SGC) e ISO 27001  lo cual tiene como propósito asegurar que se cumplan todos los requerimientos regulatorios para enfrentar los riesgos.
				        </p>

                <h3>ENFOQUE TRIVIAL DE LA AUDITORÍA INTEGRAL</h3>
                <p>
                  Este enfoque permite evaluar conjuntamente todas las áreas de mayor riesgo y es así que todos estos departamentos se integran y conforman el enfoque integrado de auditoria, permitiéndole ser una herramienta de vigilancia y apoyo para el cumplimiento  de los objetivos de las organizaciones.
                  En  la actualidad las auditorias pasaron a ser de una revisión solo contable a una revisión exhaustiva tanto de los estados financieros como de la gestión administrativa,  es por eso que la auditoría integral va más allá de solo buscar la razonabilidad de los estados financieros, sino también va en busca de la razonabilidad del control  interno y del cumplimiento legal, este tipo de auditoria ha ido evolucionando ya que nació en el sector publico debido a la necesidad de obtener información eficiente de todas las operaciones del sector público.
                </p>
                <p>
                  El objetivo fundamental de este enfoque es evaluar tanto financiero, económico y la gestión administrativa de la empresa, entre otras actividades que se de en determinada organización, nos especifica que nos ayuda a la toma de decisiones y determina si los administradores usan correctamente los bienes y servicios en cantidad y calidad, recursos humanos y financieros y si sus programas y  proyectos logran alcanzar las metas propuestas para el buen funcionamiento de todas las partes que conforman la empresa. En la ilustración número dos se presenta como la auditoría se relaciona a las diferentes áreas de las empresas.
                  Los resultados de una auditoría integral ayudaría a la toma de decisiones y a determinar si los administradores de la empresa están usando adecuadamente los principios de economía, eficiencia, eficacia, agregando la equidad, ética, y la ecología, estos  conceptos son conocidos en la literatura como las seis E. La auditoría integral examina la información financiera, el cumplimiento de las normas, leyes y políticas que regulan la actividad del ente, la eficiencia y eficacia de todos los recursos que dispone la empresa, determina si se cumplió o no con los objetivos, y evalúa la ética, equidad y la ecología.
                  Economía, Se refiere a los términos y condiciones en la que se adquieren los bienes y servicios en cantidad y calidad apropiada, y al menor costo posible.
                  Eficiencia, Consiste en la buena utilización de los bienes materiales y recursos humanos y financieros.
                  Eficacia, Verifica  que los programas y proyectos cumplan los objetivos y metas propuestas.
                  Equidad.- Que exista igualdad social y empresarial en todos sus recursos tanto financiero, económico y humano.
                  Ética, Su finalidad es crear conciencia de responsabilidad en los empleados de la empresa.
                  Ecología, Verifica el cumplimiento de normas por parte de la empresa y de la sociedad  para la preservación del medio ambiente y su sostenibilidad.
                </p>
